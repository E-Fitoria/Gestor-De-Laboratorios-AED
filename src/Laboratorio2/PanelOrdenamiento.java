    /*
    Nota: Para que se seleccione un radioboton a la vez se debe crear un bottonGroup
    Se deben modificar la propiedad bottonGroup de cada radiobotton y agregarlo al bottonGroup que acabamos de crear
    
    Se usa la funcion isSelected() para saber si el botton esta presionado
    */
    public void Mostrar (int[] array_desordenado){
        
        if (RadioButtonBurbuja.isSelected())
        {
            TextFieldMostrar.setText(Burbuja(array_desordenado));
        }
        else if (RadioButtonInsercion.isSelected())
        {
            TextFieldMostrar.setText(Inserccion(array_desordenado));
        }
        if (RadioButtonSacudida.isSelected())
        {
            TextFieldMostrar.setText(Sacudida(array_desordenado));
        }
        else if (RadioButtonSeleccion.isSelected())
        {
            TextFieldMostrar.setText(Seleccion(array_desordenado));
        }      
        else if (RadioButtonShell.isSelected())
        {
            TextFieldMostrar.setText(Shell(array_desordenado));
        }
    }

