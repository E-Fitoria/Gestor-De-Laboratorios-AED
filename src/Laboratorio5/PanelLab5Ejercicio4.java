    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
       textIngresado.setText("");
        try
       {
            lista.remove(lista.get(lista.size()-1)); //borramos el ultimo elemento de la lista
            modelo.removeRow(lista.size()); //a nuestro modelo le borramos la fila donde se encuentra el ultimo elemento
            jTablePilas.setModel(modelo);
       }
       catch (Exception e)
       {
           JOptionPane.showMessageDialog(null,"La lista está vacía\nIngrese valores para eliminar");
       }  
    }//GEN-LAST:event_btnEliminarActionPerformed


