    /**
     * Tomamos el valor ingresado por el usuario y lo agreamos a la lista con add(valor) eneste caso muestra 
     * lista es para tipos string, sabemos que para agregar una nueva fila le debemos pasar un vector a la 
     * funcion addRow, entonces creamos un vector de tipo string con el valor del ultimo elemento agregado a la 
     * lista, con get() conseguimos el elemento y con size() la longitud de la lista si le restamos un 1 
     * encontramos el ultimo valor agregado
     * 
     */
    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
          String cadena = textIngresado.getText();
          lista.add(cadena);
          String [] cadena2 = new String[1];
          cadena2[0] = lista.get(lista.size()-1);
          modelo.addRow(cadena2); //agregamos una fila con los datos de cadena2 a nuestro modelo que estamos creando
          jTablePilas.setModel(modelo);  //mostramos nuestro modelo de la tabla en el jtable
          textIngresado.setText(""); 
    }//GEN-LAST:event_btnInsertarActionPerformed

